function showAllInfoWarning() {
    var x = document.getElementById("showAllInfoWarning");
    if (x.style.display === "block") {
        x.style.display = "none";
        x.style.transitionDuration = "1s";
        
    } else {
        x.style.display = "block";
        
    }
}
 
